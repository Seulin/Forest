class Player:
    def __init__(self, x=0, y=0):
        self.x = x
        self.y = y
    def move(self, dir):
        if dir =='up':
            self.y += 1
        elif dir =='down':
            self.y -= 1
        elif dir =='left':
            self.x -= 1
        elif dir =='right':
            self.x += 1
        pass
    def get_position(self):
        return (self.x, self.y)

class MazeGame:
    def __init__(self, map=None):
        self.map = map
        self.player = Player()
        self.path = []

    def play(self):
        a = len(self.map)
        D = ['up', 'right', 'down', 'left']
        x = 4
        while self.player.get_position() != (a - 1, a - 1):
            self.player.move(D[(4 + x + 0) % 4])
            if 0 <= self.player.x <= a - 1 and 0 <= self.player.y <= a - 1:
                if self.map[self.player.y][self.player.x] == 0:
                    self.path.append(self.player.get_position())
                    x += -1
                else:
                    self.player.move(D[(4 + x + 0 + 2) % 4])
                    self.player.move(D[(4 + x + 1) % 4])
                    if 0 <= self.player.x <= a - 1 and 0 <= self.player.y <= a - 1:
                        if self.map[self.player.y][self.player.x] == 0:
                            self.path.append(self.player.get_position())
                            x += 0
                        else:
                            self.player.move(D[(4 + x + 1 + 2) % 4])
                            x += 1
                    else:
                        self.player.move(D[(4 + x + 1 + 2) % 4])
                        x += 1
            else:
                self.player.move(D[(4 + x + 0 + 2) % 4])
                self.player.move(D[(4 + x + 1) % 4])
                if 0 <= self.player.x <= a - 1 and 0 <= self.player.y <= a - 1:
                    if self.map[self.player.y][self.player.x] == 0:
                        self.path.append(self.player.get_position())
                        x += 0
                    else:
                        self.player.move(D[(4 + x + 1 + 2) % 4])
                        x += 1
                else:
                    self.player.move(D[(4 + x + 1 + 2) % 4])
                    x += 1

    def get_map(self):
        return self.map
    def get_path(self):
        return self.path

